CREATE TABLE GameLogs (
	GAME_DATE VARCHAR(8),
	GAME_NUMBER VARCHAR(1),
	DAY_OF_WEEK VARCHAR(4),
	VISITING_TEAM VARCHAR(3),
	VISITING_LEAGUE VARCHAR(2),
	VISITING_GAME_NUMBER INT,
	HOME_TEAM VARCHAR(3),
	HOME_LEAGUE VARCHAR(2),
	HOME_GAME_NUMBER INT,
	VISITING_SCORE INT,
	HOME_SCORE INT,
	OUTS INT,
	DAY_NIGHT VARCHAR(1),
	COMPLETION_CODE VARCHAR(30),
	FORFEIT_CODE VARCHAR(1),
	PROTEST_CODE VARCHAR(2),
	PARK VARCHAR(5),
	ATTENDANCE INT,
	LENGTH_MINUTES INT,
	VISITING_LINE VARCHAR(20),
	HOME_LINE VARCHAR(20),
	PRIMARY KEY (GAME_DATE, GAME_NUMBER, VISITING_TEAM, HOME_TEAM)
)